public class OperacaoMat {
    public static void main(String[] args) {
        // AUTO INCREMENTO E DECREMENTO

        int numA = 100;
        numA += 200; // numA = numA + 200

        int numB = 100;
        numB *= 4; //numB = numB * 4;

        int numC = 10555;
        numC %= 5; // numC = numC % 5;

        System.out.println(numA +", "+ numB +", "+ numC);
    }

}
